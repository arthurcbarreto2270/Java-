package org.example;

public class Tipos {
    public static void main(String[] args) {
        // Declaração de variavéis
        String  nome = "Marta";
        int idade = 39;
        double peso = 65.2;
        // Declaração de constantes
        final String CPF = "123.456.789-00";

//        Executando resultados
//        Concatenando com o sinal de "+"
        System.out.println("O nome de usuário é " + nome);
        System.out.println("O CPF é do usuário é: " + CPF);
        System.out.println("A idade do usuário é: " + idade + " anos");
        System.out.println("O peso usuário é: " + peso + " kg");
    }
}
