package org.example;

public class Relacionais {
    public static void main(String[] args) {
//        Declaração de variaveis
        int a = 10;
        int b = 2;
        int maior;

//        Operadores aritmeticos
        if (a > b) {
            maior = a;
        }else{
            maior = b;
        }

        System.out.println("O maior número é: " + maior);
    }
}
